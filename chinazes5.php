<?php

// Определение массива с текстовыми строками
$texts = [
    "Это пример строки",
    "Это более длинная строка",
    "Это самая длинная строка в этом массиве",
    "Это короткая строка",
    "Еще одна длинная строка для примера"
];

// Нахождение самой длинной строки в массиве
$longestString = '';
foreach ($texts as $text) {
    if (strlen($text) > strlen($longestString)) {
        $longestString = $text;
    }
}

// Вывод самой длинной строки
echo "Самая длинная строка: " . $longestString . "\n";

?>
